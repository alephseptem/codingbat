public boolean sleepIn(boolean weekday, boolean vacation) {
  if (weekday == true && vacation == true)
  {
    return true;
  }
  if (weekday == false && vacation == true)
  {
    return true;
  }
  if (weekday == true && vacation == false)
  {
    return false;
  }
  if (weekday == false && vacation == false)
  {
    return true;
  }
  return false;
}
